public class Tv {

    public void turnOn() {
        System.out.println("TV를 켰습니다.");
    }

    public void turnOff() {
        System.out.println("TV를 껐습니다.");
    }

    public void volumeUp() {
        System.out.println("볼륨을 높입니다.");
    }

    public void volumeDown() {
        System.out.println("볼륨을 낮춥니다.");
    }
}
